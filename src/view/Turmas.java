    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String codigo = campoCodigo.getText();
        String disciplina = campoDisciplina.getText();
        String professorNome = campoProfessor.getText();
        String curso = campoCurso.getText();
        String horario = campoHorario.getText();

        if (codigo.isEmpty() || disciplina.isEmpty() || professorNome.isEmpty() || 
            curso.isEmpty() || horario.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        Professor professor = new Professor();
        professor.setNome(professorNome);
        professor.setDisciplina(disciplina);

        controller.GerenciadorTurmas gerenciador = new controller.GerenciadorTurmas();
        gerenciador.criarTurma(codigo, disciplina, professor, curso, horario);

        javax.swing.JOptionPane.showMessageDialog(this, "Turma criada com sucesso!");

        campoCodigo.setText("");
        campoDisciplina.setText("");
        campoProfessor.setText("");
        campoCurso.setText("");
        campoHorario.setText("");

        atualizarTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

