    private void atualizarTabela() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    controller.GerenciadorTurmas gerenciador = new controller.GerenciadorTurmas();
    
    for (turmas turma : gerenciador.getTodasTurmas()) {
        model.addRow(new Object[]{
            turma.getCodigoTurma(),
            turma.getDisciplina(),
            turma.getCurso(),
            turma.getProfessor() != null ? turma.getProfessor().getNome() : "N/A",
            turma.getHorario(),
            //turma.getAlunos().size()
        });
    }
}
