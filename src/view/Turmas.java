    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            String codigoTurma = (String) jTable1.getValueAt(selectedRow, 0);
            controller.GerenciadorTurmas gerenciador = new controller.GerenciadorTurmas();
            turmas turma = gerenciador.buscarTurmaPorCodigo(codigoTurma);

            if (turma != null) {
                campoCodigo.setText(turma.getCodigoTurma());
                campoDisciplina.setText(turma.getDisciplina());
                campoProfessor.setText(turma.getProfessor() != null ? turma.getProfessor().getNome() : "");
                campoCurso.setText(turma.getCurso());
                campoHorario.setText(turma.getHorario());

                javax.swing.JOptionPane.showMessageDialog(this, "Dados carregados para edição!");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma turma para editar!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

