package model;

import java.io.Serializable;

/**
 *
 * @author liliano
 */
public abstract class Pessoa implements Serializable {
    protected String nome;
    protected String bi;
    protected int idade;
    protected String genero;
    protected String email;

    public Pessoa(String nome, String bi, int idade, String genero, String email) {
        this.nome = nome;
        this.bi = bi;
        this.idade = idade;
        this.genero = genero;
        this.email = email;
    }

    public Pessoa() {
    }
    
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getBi() {
        return bi;
    }

    public void setBi(String bi) {
        this.bi = bi;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
